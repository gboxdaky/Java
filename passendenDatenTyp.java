public class passendenDatenTyp {
    public static void main(String[] args) {

        //Distanz 
        int dist; 
        dist = 5000; 
        System.out.println("Distanz  = " + dist); 

        // Kilos
        byte kg;
        kg = 15;
        System.out.println("Kilos = " + kg); 

        // Alter 
        int age;
        age = 36;
        System.out.println("Alter  = " + age); 

        // Konto 
        int saldo;
        saldo = -2900;
        String chf = "CHF im Minus";

        System.out.println("Saldo = " + saldo + " " + chf); 
    }
}
